    /**
     * @param $cnt
     * @return string
     */
    protected function getCyrillicName($cnt = 2)
    {
        $names = $this->cyrillicNames();
        $namesCnt = count($names);

        $str = [];
        for ($i = 0; $i < $cnt; $i++) {
            $str[] = trim($names[mt_rand(0, $namesCnt - 1)]);
        }

        return implode(' ', $str);
    }
